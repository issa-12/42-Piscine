#include <unistd.h>
#include <stdlib.h>

int	*ft_range(int min, int max)
{
	unsigned int	i;
	int				*array;

	i = 0;
	if (min >= max)
		return (NULL);
	array = (int *)malloc(sizeof(int) * (max - min));
	while (min < max)
	{
		array[i] = min;
		min++;
		i++;
	}
	return (array);
}
